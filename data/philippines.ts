// Helper functions
export const getRegions = (): string[] => {
  return philippinesData.map((r) => r.name)
}

export const getProvincesByRegion = (regionName: string): string[] => {
  const region = philippinesData.find((r) => r.name === regionName)
  return region ? region.provinces.map((p) => p.name) : []
}

export const getBarangaysByProvince = (
  regionName: string,
  provinceName: string,
): string[] => {
  const region = philippinesData.find((r) => r.name === regionName)
  if (!region) return []
  const province = region.provinces.find((p) => p.name === provinceName)
  return province ? province.barangays : []
}

